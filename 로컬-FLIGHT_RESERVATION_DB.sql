CREATE OR REPLACE TRIGGER UPDATE_CUSTOMER_MILEAGE_TRIGGER
AFTER INSERT OR UPDATE OF QUANTITY ON RESERVATION
FOR EACH ROW
WHEN (NEW.QUANTITY > NVL(OLD.QUANTITY, 0))
BEGIN
    UPDATE CUSTOMER
    SET MILEAGE = MILEAGE + (:NEW.QUANTITY - NVL(:OLD.QUANTITY,0)) * 100
    WHERE ID = :NEW.ID;
END;
/
